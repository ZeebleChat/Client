export function searchEmojis(query: string, limit = 10): EmojiEntry[] {
  if (!query) return [];
  const q = query.toLowerCase();
  const exact: EmojiEntry[] = [];
  const starts: EmojiEntry[] = [];
  const contains: EmojiEntry[] = [];
  for (const entry of EMOJIS) {
    if (entry.n === q) exact.push(entry);
    else if (entry.n.startsWith(q)) starts.push(entry);
    else if (entry.n.includes(q)) contains.push(entry);
    if (exact.length + starts.length + contains.length >= limit * 3) break;
  }
  return [...exact, ...starts, ...contains].slice(0, limit);
}
